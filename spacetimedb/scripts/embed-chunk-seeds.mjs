/**
 * Writes spacetimedb/src/chunkSeeds.ts from public/levels/chunk_*_*.chunk.json
 * so the module can boot with the same baselines clients load from disk.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '../..');
const levelsDir = path.join(repoRoot, 'public', 'levels');
const outFile = path.resolve(__dirname, '../src/chunkSeeds.ts');

const files = fs.readdirSync(levelsDir).filter((f) => /^chunk_\d+_\d+\.chunk\.json$/u.test(f));
const seeds = {};
for (const f of files) {
  const m = /^chunk_(\d+)_(\d+)\.chunk\.json$/u.exec(f);
  if (!m) continue;
  const key = `${m[1]},${m[2]}`;
  seeds[key] = fs.readFileSync(path.join(levelsDir, f), 'utf8');
}

const banner =
  '// AUTO-GENERATED by scripts/embed-chunk-seeds.mjs — do not edit by hand.\n\n';
const body = `export const TERRAIN_CHUNK_SEEDS: Record<string, string> = ${JSON.stringify(seeds, null, 2)};\n`;
fs.writeFileSync(outFile, banner + body, 'utf8');
console.log(`Wrote ${Object.keys(seeds).length} chunk seeds to ${path.relative(repoRoot, outFile)}`);
